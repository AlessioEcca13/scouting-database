/**
 * Translate a text from Italian to English
 * @param {string} text - Text to translate
 * @param {string} category - Category (optional)
 * @returns {string} Translated text
 */
export const translate = (text, category = null) => {
  if (!text) return text;

  // If category specified, search in that category
  if (category && translations[category] && translations[category][text]) {
    return translations[category][text];
  }

  // Search in all categories
  for (const cat in translations) {
    if (translations[cat][text]) {
      return translations[cat][text];
    }
  }

  // Return original if not found
  return text;
};

/**
 * Translate player role
 */
export const translateRole = (role) => {
  return translations.roles[role] || role;
};

/**
 * Translate position abbreviation
 */
export const translatePosition = (position) => {
  return translations.positions[position] || position;
};

/**
 * Translate position to full name
 */
export const translatePositionFull = (position) => {
  return translations.positionsFull[position] || position;
};

/**
 * Translate foot
 */
export const translateFoot = (foot) => {
  if (!foot) return foot;
  const footLower = foot.toLowerCase();
  return translations.foot[footLower] || translations.foot[foot] || foot;
};

export default translations;
